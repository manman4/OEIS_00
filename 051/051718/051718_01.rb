def A(k, n)
  ary = []
  a = []
  (0..n + k).each{|i|
    a << 1r / (i + 1)
    i.downto(1){|j| a[j - 1] = j * (a[j - 1] - a[j])}
    ary << a[k].numerator if i >=  k
  }
  ary
end

n = 650
ary = A(2, n)
(0..n).each{|i|
  j = ary[i]
  break if j.to_s.size > 1000
